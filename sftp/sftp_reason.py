from enum import IntEnum


class SftpReason(IntEnum):
    OK = 0x0000
    INVALID_SYSTEM_ID = 0x0001
    INVALID_PROTOCOL_ID = 0x0002
    INVALID_CLIENT_SIDE = 0x0100
    INVALID_OPERATION = 0x0101
    INVALID_LENGTH = 0x0102
    INVALID_CRC = 0x0103
    NO_TARGET_FILE = 0x0104
    TIMEOUT = 0x0105
    TCP_SOCKET_ERROR = 0x0200
    NO_REASON = 0xFFFF
